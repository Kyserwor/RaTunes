public class Artist {
    private int id;
    private String name;
    private int albumID;


    public Artist(){
        this.id = 0;
        this.name = null;
        this.albumID = 0;
    }

    public Artist(int id, String name, int albumID) {
        this.id = id;
        this.name = name;
        this.albumID = albumID;
    }

    //region Getter and Setter
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAlbumID() {
        return albumID;
    }

    public void setAlbumID(int albumID) {
        this.albumID = albumID;
    }
    //endregion
}
